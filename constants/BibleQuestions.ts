export const getRandomQuestion = (): BibleQuestion => {
  return BIBLE_QUESTIONS[Math.floor(Math.random() * BIBLE_QUESTIONS.length)];
};

export const getQuestionsByDifficulty = (difficulty: 'easy' | 'medium' | 'hard'): BibleQuestion[] => {
  return BIBLE_QUESTIONS.filter(q => q.difficulty === difficulty);
};

export const getMultipleQuestions = (count: number, difficulty?: 'easy' | 'medium' | 'hard'): BibleQuestion[] => {
  let questions = difficulty ? getQuestionsByDifficulty(difficulty) : BIBLE_QUESTIONS;
  const shuffled = [...questions].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, Math.min(count, questions.length));
};
